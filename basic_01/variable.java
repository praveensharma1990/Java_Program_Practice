package basic_01;

public class variable {
    public static void main(String[] args) {
        //primitive data types variables
        int a=10; byte b=3; char cr='A'; double salary= 39999.3;
        boolean M= true; short sh= 333; float ft= 333f; long lt=44444444L;

        System.out.println("value of int type variable  "+a);
        System.out.println("value of byte type variable  "+b);
        System.out.println("value of char type variable  "+cr);
        System.out.println("value of double type variable  "+salary);
        System.out.println("value of boolean type variable  "+M);
        System.out.println("value of short type variable  "+sh);
        System.out.println("value of float type variable  "+ft);
        System.out.println("value of long type variable  "+lt);
    }


}
